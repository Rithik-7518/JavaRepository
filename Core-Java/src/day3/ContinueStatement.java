package day3;

public class ContinueStatement {

	public static void main(String[] args) {
		for(int i=1;i<=10;i++) 
		{
			if (i==6) 
			{
				
				continue; // jump to next not print 6
			}
			System.out.println(i);
		}
		
		/*
		 * reserve number from given number
		 * palindrome number
		 * count number of digit in a number
		 * find sum of digit in a number 
		 */
	}

}
