package day3;

public class dowhileLoop {

	public static void main(String[] args) {

		System.out.println("output for first doWhile loop");
		int i=1;
		do {
			
			System.out.println(i);
			i++;
			
		}while(i<=10);
		
		
		System.out.println("output for second doWhile loop");
		int j=10;
		do {
			
			System.out.println(j);
			j--;
			
		}while(j>0);
		
	}

}
