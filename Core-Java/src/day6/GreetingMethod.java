package day6;

public class GreetingMethod {
	

		//methods can be create by below 1,2,3,4
		
		// 1)--->no params and no return value
		void m1() {
			System.out.println("Hello");
			
		}
		
		
		// 2)---> no params and return value
		String m2() 
		{
			return("hii...");
		}
		
		// 3)---> take params and no return value
		void m3(String name) {
			System.out.println("helloo "+name);
		}
		
		// 4)--->take params and return value
		String m4(String name) {
			return("helloo "+name);
		}
		
		


}
