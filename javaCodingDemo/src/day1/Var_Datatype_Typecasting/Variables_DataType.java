package day1.Var_Datatype_Typecasting;

public class Variables_DataType {

	public static void main(String args[]) {
		System.out.println("compling is started ");
		//integer
		int a=7;
		byte b=127;
		short c=5465;
		long d=2345l;
		
		//float
		float e=2.4f;
		double f=2.3;
		
		//charater
		char g='R';
		
		//boolean
		boolean h=true;
		System.out.println("compling is ended");
		
	}
}
